/* Decode current runlength */
static u16 get_rle(u8 **ptr)
{
	unsigned ret;

	if (**ptr & 0x80) {
		ret = **ptr << 8;
		(*ptr)++;
		ret |= **ptr;
		(*ptr)++;
		ret &= ~0x8000;
	} else {
		ret = **ptr;
		(*ptr)++;
	}
	return ret;
}

/** Draw the splash screen logo at the center of the screen
 * @fbinfo: Coreboot framebuffer to draw into
 */
void cbui_draw_splash(struct cb_framebuffer *fbinfo)
{
	u32 *front_buffer = phys_to_virt(fbinfo->physical_address);
	u32 offset_x, offset_y, x, y;
	u16 i, rl;
	u8 *ptr = cbui_logo.data;
	u8 color = !!cbui_logo.first_pixel_c;

	if ((cbui_logo.width > fbinfo->x_resolution) ||
		(cbui_logo.heigth > fbinfo->y_resolution))
		return;

	offset_x = fbinfo->x_resolution / 2 - cbui_logo.width / 2;
	offset_y = fbinfo->y_resolution / 2 - cbui_logo.heigth / 2;

	/* Reset run counter */
	i = 0;
	/* Set initial run length */
	rl = get_rle(&ptr);

	/* Draw run length encoded logo */
	for (y = 0; y < cbui_logo.heigth; y ++) {
		for (x = 0; x < cbui_logo.width; x ++) {
			front_buffer[((y + offset_y) * fbinfo->x_resolution) + x + offset_x] = color ? ~0 : 0;
			if (i++ == rl) {
				i = 0;
				rl = get_rle(&ptr);
				color ^= 1;
			}
		}
	}
}

/** Draw a progress bar right below the logo.
 * @fbinfo: Coreboot framebuffer to draw into
 * @perc: Progress 0xff maps to 100%
 */
void cbui_draw_splash_bar(struct cb_framebuffer *fbinfo, u8 perc)
{
	u32 *front_buffer = phys_to_virt(fbinfo->physical_address);
	u32 offset_x, offset_y, x, y;
	u32 height = fbinfo->y_resolution / 64;
	u32 color;

	/* Opt out on invalid logo dimensions */
	if ((cbui_logo.width > fbinfo->x_resolution) ||
		(cbui_logo.heigth > fbinfo->y_resolution))
		return;

	/* Place bar right under logo */
	offset_x = fbinfo->x_resolution / 2 - cbui_logo.width / 2;
	offset_y = fbinfo->y_resolution / 2 + cbui_logo.heigth / 2 + height * 2;

	if (offset_y > (fbinfo->y_resolution - height - 1))
		offset_y = fbinfo->y_resolution - height - 1;

	/* Use logo width as horizontal size */
	for (x = 0; x < cbui_logo.width; x ++) {
		color = (x > ((perc * cbui_logo.width) >> 8)) ? 0 : ~0;
		for (y = 0; y < height; y ++) {
			front_buffer[((y + offset_y) * fbinfo->x_resolution) + x + offset_x] = color;
		}
	}
}

